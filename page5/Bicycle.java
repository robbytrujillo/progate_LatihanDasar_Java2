class Bicycle {
  private String name;
  private String color;
  private int distance;
  private int run;
  Bicycle(String name, String color, int distance, int run) {            
  this.name = name;  
  this.color = color;
  this.distance = distance;
  this.run = run;
  }            
                 
   public String getName() {            
    return this.name;            
    }            
    
    public String getColor() {
      return this.color;
    }    
    
    public int getDistance() {
      return this.distance;
    }
    
    public int getRun() {
      return this.run;
    }
    }          